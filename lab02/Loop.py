thislist = ["apple", "banana", "cherry"]
for x in thislist:
  print(x)
#prints every element from the list

thislist = ["apple", "banana", "cherry"]
for i in range(len(thislist)):
  print(thislist[i])
#same thing but other way

thislist = ["apple", "banana", "cherry"]
[print(x) for x in thislist]
