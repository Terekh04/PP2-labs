thislist = ["apple", "banana", "cherry"]
print(thislist[1])
#"banana"

thislist = ["apple", "banana", "cherry"]
print(thislist[-1])
#"cherry"

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5])
#"cherry","orange","kiwi"(from 2 to 5 not icluded)

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[:4])
#from 0 to 4

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:])
#from 2 to the end

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[-4:-1])
#from "orange" to "melon"

